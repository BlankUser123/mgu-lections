public class Program {

    public static void main(String[] args) {
        int num = 10;

        switch(num) {
            case 8: {
                System.out.println("Число дорівнює 8");
                break;
            }

            case 9: {
                System.out.println("Число дорівнює 9");
                break;
            }

            default: {
                System.out.println("Число не дорівнює 8 або 9");
                break;
            }
        }
    }
}
